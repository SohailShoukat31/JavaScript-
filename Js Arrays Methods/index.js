// Arrays methods

// push()
const fruits = ["Apple", "Banana", "Orange", "Mango"];
console.log(fruits);

fruits.push("Kiwi");
console.log(fruits);

// shift()
const fruits2 = ["Apple", "Banana", "Orange", "Mango"];
console.log(fruits2);
fruits2.shift();
fruits2.shift();
console.log(fruits2);

// unshift()

const fruits3 = ["Apple", "Banana", "Orange", "Mango"];
console.log(fruits3);
fruits3.unshift("Kiwi");
console.log(fruits3);

// pop()

const fruits4 = ["Apple", "Banana", "Orange", "Mango"];
console.log(fruits4);
fruits4.pop();
console.log(fruits4);


